

<h1>Què son els reptes?	</h1>

<h3>Aconsegueix cromos reptant altres usuaris!</h3>

<p>
	Pots reptar a altres usuaris, la condició per a reptar altres usuaris és que 
	col·leccioneu el mateix àlbum.<br />
	Al reptar, competeixes amb un altre usuari per aconseguir un cromo d'un àlbum que els dos col·leccioneu.<br />
</p>
<p>
	A l'acceptar un repte, els dos usuaris us enfornteu a unes preguntes, el què n'encerti més guanya el repte i per tant guanya el cromo per el qual s'està competint.<br/>
</p>
<h5>
	Repta a altres usuaris per a aconseguir més cromos.
</h5>