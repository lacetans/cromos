		<h1>Política de privacitat</h1>

			<h4>1. Protecció i tractament de dades de caràcter personal</h4>

			<p>cromos.cat, titular del domini d’aquest web, es compromet a respectar la legislació vigent de protecció i confidencialitat en el tractament de dades de caràcter personal, d’acord amb el que disposa la Llei orgànica 15/1999, de 13 desembre, de protecció de dades de caràcter personal, i la seva normativa de desplegament.</p>

			<h4>2. Galetes</h4>

			<p>En cap cas les galetes (cookies) o altres mitjans de naturalesa anàloga s'utilitzen per emmagatzemar informació que permeti identificar la persona física usuària del web.</p>

			<h4>3. Protecció de dades personals incloses en els formularis</h4>

			<p>D’acord amb el que estableix la Llei orgànica 15/1999, de 13 de desembre, de protecció de dades de caràcter personal, les dades personals que faciliteu en el formulari de demandes del web seran objecte de tractament i s’inclouran en el fitxer informatitzat, del qual joanramon.hol.es n’és el responsable, amb l’exclusiva finalitat de mantenir la relació administrativa per gestionar la sol·licitud d’informació, enviar informació per correu postal i electrònic sobre serveis o activitats de joanramon.hol.es en relació amb les seves competències, i adreçar-vos qüestionaris de resposta voluntària, per mitjans tradicionals i electrònics, sobre el nivell de satisfacció referida a la qualitat del servei, a fi de poder-lo ampliar i millorar, si és el cas.</p>

			<h4>4. Cessió de dades de caràcter personal</h4>

			<p>cromos.cat es compromet a no comunicar les dades de caràcter personal subministrades per la persona interessada per cap mitjà i a no vendre, ni llogar, ni cedir les dades personals dels usuaris dels formularis a cap empresa o entitat pública o privada sense el consentiment del titular de les dades. En tot cas, les dades personals només seran cedides als òrgans judicials i a autoritats administratives si una llei ho estableix preceptivament.</p>

			<h4>5. Seguretat de les dades de caràcter personal</h4>

			<p>Les dades de caràcter personal seran tractades amb el grau de protecció legalment requerit, segons el Reial decret 994/1999, d’11 de juny, i es prendran les mesures de seguretat necessàries per evitar-ne el mal ús, l’alteració, la pèrdua, el tractament o l’accés no autoritzat i la sostracció per part de tercers, els quals les poden utilitzar per a finalitats fraudulentes o diferents d’aquelles per a les quals han estat sol·licitades a l’usuari.</p>

			<h4>6. Drets de les persones interessades</h4>

			<p>Si la persona interessada vol qualsevol aclariment sobre la confidencialitat o el tractament que reben les seves dades, o bé si desitja exercir algun dels drets d’informació, oposició, accés, rectificació i cancel·lació que legalment li corresponen (o qualsevol altre dret que cregui que la pot assistir), pot dirigir-se a l'INS Lacetània.</p>


